public class Main {
    public static void main(String[] args) {
        System.out.println("\nGroup Z - Example\n");
        for (int i = 0; i < 5; i++)
            System.out.print(i + " ");
        System.out.println();
        for (int i = 80; i > 70; i--)
            System.out.print(i + " ");
        System.out.println();
        System.out.println("\nGroup A - Counting\n");
        for (int i = 0; i <= 12; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 1; i <= 19; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 12; i <= 22; i++) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 101; i <= 115; i++) {
            System.out.print(i + " ");
        }
        System.out.println("\nGroup B - Backwards\n");
        for (int i = 12; i >= 0; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 20; i >= 1; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 22; i >= 12; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");
        for (int i = 115; i >= 101; i--) {
            System.out.print(i + " ");
        }
        System.out.println(" ");

    }
}
